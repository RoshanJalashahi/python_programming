# string can be modify
# the upper() method returns the string with uppercase
a ="prabina"
print(a.upper())

# lower () method
a ="HELLO, Prabina"
print(a.lower())

# removing whitespace using strip() method
# this method removes spave from the beginning or from the end but not from the middle
a =" Prabina, Roshan! "
print(a.strip())

#replace() method
a = " hello, Hrabina"
print(a.replace("H", "P"))

#split() method
a = " Roshan, Prabina"
print(a.split(","))